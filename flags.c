#include "ft_printf.h"

int	flags(int c)
{
	int		i;
	char	*flags;

	i = 0;
	flags = "-0 .*";
	while(flags[i] !='\0')
	{
		if(c == (int)flags[i])
			return (1);
		i++;
	}
	return (0);
}
